package com.PTIV.loja.de.games.model;

public class Quantity {

    private Integer id;


    private Integer value;

    public Quantity() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

}